package SebaJava;

public class TiposDeDatosPrimitivos {
	public static void main (String args []) {
		
	/*DATOS PRIMITIVOS
	 Son datos que "etiquetan" a las variables. 
	 No necesitan ser "invocados". 
	 Acordate: variable es una palabrita que guarda
	 texto o números en la memoria de la pc.
	 
	 En el classroom te dejé cuáles son esos datos
	 primitivos: int, float, char, etc.
	 */
	
	// Ejemplo
	
	char palabra = 'a'; // los caracteres se escriben con ''
	System.out.println(palabra);
	
	// Otro ejemplo
	
	int num = 4;
	System.out.println(num);
	
	}
	
	
}
